class VOCTestDataset:
    def __init__(self, cfg, split='trainval', use_difficult=True):
        self.cfg = cfg
        self.db = VOCBboxDataset(cfg.DATASETS.DATASET_ROOT, split=split, use_difficult=use_difficult)

    def __getitem__(self, idx):
        ori_img, bbox, label, difficult = self.db.get_example(idx)
        img = preprocess(ori_img)
        return img, ori_img.shape[1:], bbox, label, difficult

    def __len__(self):
        return len(self.db)
